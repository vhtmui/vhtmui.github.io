	<h1>学习 Web 开发</h1>
	<div class="section-content">
		<p>
			欢迎来到 MDN 学习区。本系列文章意在为 Web
			开发的纯新手提供编写网站的基本技巧，而非意在让你从“新手”变成“专家”，但能让你从“新手”变得“熟练”。从那之后，你就能开始以你自己的方式学习
			<a href="https://www.baidu.com">MDN 的其余部分</a>，和其他需要大量前置知识的中高级资源。
		</p>
		<p>
			从零开始学习 Web
			开发极具挑战性，该教程将为你提供详细的资料，手把手帮助你轻松愉快地学习。无论你是正在学习 Web
			开发的学生（自学或参与课程）、寻找材料的老师、编程爱好者，抑或是仅想进一步了解 Web
			技术的工作原理，我们都希望你能感到宾至如归。
		</p>
	</div>
	<a href="#学习起点">学习起点</a>
	<section aria-labelledby="学习起点">
		<h2 id="学习起点"><a href="#学习起点">学习起点</a></h2>
		<div class="section-content">
			<p>
				你要是还不确定是否要深入地学习 Web 开发，仅想从体验环节开始，我们会建议你从 <a
					href="/zh-CN/docs/Learn/Getting_started_with_the_web">Web 入门</a
				>指南开始学习。除此之外，你应该从下面的专题开始学习。
			</p>
			<dl>
				<dt id="html_和_css"><a href="#html_%E5%92%8C_css">HTML 和 CSS</a></dt>
				<dd>
					<p>
						HTML 为 Web 内容提供结构，而 CSS 为 Web 内容添加样式并对 Web 内容进行布局。在 <a
							href="/zh-CN/docs/Learn/HTML/Introduction_to_HTML">HTML 简介</a
						>和 <a href="/zh-CN/docs/Learn/CSS/First_steps">CSS 第一步</a>中学习基本的入门知识。
					</p>
				</dd>
				<dt id="javascript"><a href="#javascript">JavaScript</a></dt>
				<dd>
					<p>
						JavaScript 为网站提供交互功能。从 <a href="www.baidu.com"
							>JavaScript 第一步</a
						>开始学习。
					</p>
				</dd>
				<dt id="框架和工具">
					<a href="#%E6%A1%86%E6%9E%B6%E5%92%8C%E5%B7%A5%E5%85%B7">框架和工具</a>
				</dt>
				<dd>
					<p>
						掌握原生 HTML、CSS 和 JavaScript 的基本知识之后，你应该学习<a
							href="/zh-CN/docs/Learn/Tools_and_testing/Understanding_client-side_tools"
							>客户端 Web 开发工具</a
						>，接着考虑钻研<a
							href="/zh-CN/docs/Learn/Tools_and_testing/Client-side_JavaScript_frameworks"
							>客户端 JavaScript 框架</a
						>。你也应该考虑学习<a href="/zh-CN/docs/Learn/Server-side">服务器端网站编程</a
						>的基础知识。
					</p>
				</dd>
			</dl>
			<div class="notecard note" id="sect1">
				<p>
					<strong>备注：</strong>在<a href="/zh-CN/docs/Glossary">术语表</a
					>中能找到术语定义。此外，要是有和 Web 开发相关的特定问题，<a
						href="/zh-CN/docs/Learn/Common_questions">常见问题</a
					>中的内容也许会帮助到你。
				</p>
			</div>
			<div class="callout" id="sect2">
				<h4 id="想成为一名前端_web_开发者吗？">想成为一名前端 Web 开发者吗？</h4>
				<p>
					你要是想成为一名前端 Web 开发者，但不确定该从哪开始学习，我们会建议使用 <a
						href="/en-US/curriculum/">MDN 课程</a
					>作为你的学习计划。它提供一个结构化的学习路径，包含：成为一名成功的前端开发者所需的基本技巧和练习，以及推荐学习资源。
				</p>
				<p><a href="/en-US/curriculum/"><strong>现在开始</strong></a></p>
			</div>
