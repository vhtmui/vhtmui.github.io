	<section aria-labelledby="获取代码示例">
		<h2 id="获取代码示例"><a href="#获取代码示例">获取代码示例</a></h2>
		<div class="section-content">
			<p>
				学习区的所有代码示例都<a
					href="https://github.com/mdn/learning-area/"
					class="external"
					target="_blank">可以在 Github 上找到</a
				>。如果你想将代码示例都复制到你的电脑，最简单的方式就是<a
					href="https://codeload.github.com/mdn/learning-area/zip/main"
					class="external"
					target="_blank">下载包含最新 main 分支内容的压缩文件</a
				>。
			</p>
			<p>如果你更喜欢以一种更灵活的方式（允许自动更新）复制仓库，你可以参考下面复杂一点的指导：</p>
			<ol>
				<li>
					在电脑上安装 <a href="https://git-scm.com/downloads" class="external" target="_blank"
						>Git</a
					>。它是 Github 底层使用的版本控制系统。
				</li>
				<li>
					打开电脑的<a
						href="https://www.lifewire.com/how-to-open-command-prompt-2618089"
						class="external"
						target="_blank">命令提示符</a
					>（Windows）或终端（<a
						href="https://help.ubuntu.com/community/UsingTheTerminal"
						class="external"
						target="_blank">Linux</a
					>、<a
						href="https://blog.teamtreehouse.com/introduction-to-the-mac-os-x-command-line"
						class="external"
						target="_blank">macOS</a
					>）。
				</li>
				<li>
					要将学习区仓库复制到名为 learning-area 的目录（learning-area
					目录将位于命令提示符/终端指向的当前目录中），使用下面的命令：
					<div class="code-example">
						<div class="example-header">
							<span class="language-name">bash</span><button type="button" class="icon copy-icon"
								><span class="visually-hidden">Copy to Clipboard</span></button
							><span class="copy-icon-message visually-hidden" role="alert"></span>
						</div>
						<pre
							class="brush: bash notranslate"
							data-signature="4IQDe+FT9F3HniKMAkuFBrdB2K5UajJUp2JTy0ZO1uA="><code
								><span class="token function">git</span> clone https://github.com/mdn/learning-area</code></pre>
					</div>
				</li>
				<li>
					现在，你能通过访达/文件资源管理器或 <a
						href="https://zh.wikipedia.org/wiki/Cd_(%E5%91%BD%E4%BB%A4)"
						class="external"
						target="_blank"><code>cd</code> 命令</a
					>进入目录并寻找你想要的文件。
				</li>
			</ol>
			<p>
				你可以将 GitHub 上的主版本的任意改动更新到本地 <code>learning-area</code> 仓库，步骤如下：
			</p>
			<ol>
				<li>
					在命令提示符/终端中使用 <code>cd</code> 命令进入 <code>learning-area</code>
