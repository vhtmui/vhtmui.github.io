	<section aria-labelledby="涵盖的专题">
		<h2 id="涵盖的专题"><a href="#涵盖的专题">涵盖的专题</a></h2>
		<div class="section-content">
			<p>下面列表包含 MDN 学习区全部的专题。</p>
			<dl>
				<dt id="web_入门"><a href="/zh-CN/docs/Learn/Getting_started_with_the_web">Web 入门</a></dt>
				<dd>
					<p>为纯新手提供实用的 Web 开发介绍。</p>
				</dd>
				<dt id="html——为_web_提供结构">
					<a href="/zh-CN/docs/Learn/HTML">HTML——为 Web 提供结构</a>
				</dt>
				<dd>
					<p>HTML 是用于构建内容的不同部分并定义其含义或目的的语言。该专题将详细讲解 HTML。</p>
				</dd>
				<dt id="css——为_web_提供样式"><a href="/zh-CN/docs/Learn/CSS">CSS——为 Web 提供样式</a></dt>
				<dd>
					<p>
						CSS 是用于为 Web 内容添加样式、对 Web 内容进行布局、添加诸如动画的行为的语言。该专题对
						CSS 做了全面的覆盖。
					</p>
				</dd>
				<dt id="javascript——动态客户端脚本语言">
					<a href="/zh-CN/docs/Learn/JavaScript">JavaScript——动态客户端脚本语言</a>
				</dt>
				<dd>
					<p>
						JavaScript 是用于为 Web 页面添加动态功能的脚本语言。该专题涵盖有关编写和理解 JavaScript
						代码所需的全部重点。
					</p>
				</dd>
				<dt id="web_表单——操作用户数据">
					<a href="/zh-CN/docs/Learn/Forms">Web 表单——操作用户数据</a>
				</dt>
				<dd>
					<p>
						Web 表单是用于和用户交互的强大工具——其常用于收集用户数据和控制用户界面。该专题涵盖 Web
						表单的结构、样式和交互相关的全部要点。
					</p>
				</dd>
				<dt id="无障碍——让每个人都能使用_web">
					<a href="/zh-CN/docs/Learn/Accessibility">无障碍——让每个人都能使用 Web</a>
				</dt>
				<dd>
					<p>
						无障碍是让更多的人不受残疾、设备、地区等因素的限制都能访问 Web
						内容的实践。该专题包含一切所需了解的信息。
					</p>
				</dd>
				<dt id="web_性能优化——让网站更快做出响应">
					<a href="/zh-CN/docs/Learn/Performance">Web 性能优化——让网站更快做出响应</a>
				</dt>
				<dd>
					<p>
						Web 性能是确保 Web
						应用的快速下载并对用户操作进行快速响应的艺术，而无论用户的带宽大小、屏幕尺寸、网络状态好坏或是设备性能高低。
					</p>
				</dd>
				<dt id="mathml"><a href="/zh-CN/docs/Learn/MathML">MathML</a></dt>
				<dd>
					<p>
						MathML 是用于在 Web
						页面中书写数学公式的语言（使用分数、上下标、根号、矩阵、积分、级数，等等）。该专题讲解
						MathML。
					</p>
				</dd>
				<dt id="工具和测试"><a href="/zh-CN/docs/Learn/Tools_and_testing">工具和测试</a></dt>
				<dd>
					<p>
						该专题涵盖的是开发者用于提升效率的工具，如跨浏览器测试工具、代码检查工具、代码格式化工具、转换工具、版本控制系统、部署工具、客户端
						JavaScript 框架等。
					</p>
				</dd>
				<dt id="服务器端网站编程"><a href="/zh-CN/docs/Learn/Server-side">服务器端网站编程</a></dt>
				<dd>
					<p>
						尽管你专注于客户端 Web
						开发，但了解服务器和服务器端代码的运行机制仍是有用的。该专题对服务器端的运作机制进行了通用介绍，以及包含使用
						Django（Python）和 Express（node.js）两个流行框架构建服务器端应用的详细教程。
					</p>
				</dd>
			</dl>
		</div>
